export const getProgrammeBySlug = (slug, locale) => {
  return programmes.find(item => {
    if (item.url) {
      if (locale) {
        return item.url[locale] === slug;
      } else {
        return Object.keys(item.url).find(lang => item.url[lang] === slug);
      }
    }
    return false;
  });
};
